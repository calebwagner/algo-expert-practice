/**
 * Hash Table, Hash Map, Map
 * a data structure that maps keys to values.
 *
 * clear() - removes all elements from a Map object.
 *
 * delete() - removes the specified element from a Map object by key.
 *
 * entries() - returns a new iterator object that contains the
 * [key, value] pairs for each element in the Map object in insertion
 * order.
 *
 * forEach() - executes a provided function once per each key/value
 * pair in the Map object, in insertion order.
 *
 * get() - returns a specified element from a Map object.
 *
 * has() - returns a boolean indicating whether an element with
 * the specified key exists or not.
 *
 * keys() - returns a new iterator object that contains the keys
 * for each element in the Map object in insertion order.
 *
 * set() - adds or updates an entry in a Map object with a
 * specified key and a value.
 *
 * values() - returns a new iterator object that contains the values
 * for each element in the Map object in insertion order.
 */