# List
# store multiple items in a single variable
# ordered, changeable, and allow duplicate values
list = ["apple", "banana", "cherry"]

# Tuple
# a collection which is ordered and unchangeable
# ordered, unchangeable, and allow duplicate values
tuple = ("apple", "banana", "cherry")

# Set
# used to store multiple items in a single variable
# unordered, unchangeable, and do not allow duplicate values
set = {"apple", "banana", "cherry"}

# Dictionary
# store data values in key/value pairs
# ordered, changeable, and does not allow duplicates
dict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}